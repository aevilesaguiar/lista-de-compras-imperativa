//entender a aplicar funções auxiliares relacionadas ao array

//crie um arra que contenha nomes de produtos para compra

let produtosCompra = ["Arroz", "feijão","batata","macarrão", "queijo", "carne", "ovos"];

//Após isso, exiba no console os resultados das funções relacionadas aos arrays, 
//que são: Join, Pop, Push, Shift e Unshift. 

console.log(produtosCompra.join(" ♥ "))//junta os arrays usando um separador

console.log(produtosCompra.pop());//elimina o ultimo elemento do array

console.log(produtosCompra.push('trigo','maionese','maçã'));//adiciona um ou mais elementos no final do array
console.log(produtosCompra);

console.log(produtosCompra.shift());//elimina o primeiro elemento de um array

console.log(produtosCompra.unshift('batata','frango','coentro'));//adiciona elementos ao inicio de um array
